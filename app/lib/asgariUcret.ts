export const ASGARI_UCRET_TABLOSU: AsgariUcretEntry[] = RAW
  .map(([tarih, aylik]) => ({ tarih: new Date(tarih), aylik }))
  .sort((a, b) => b.tarih.getTime() - a.tarih.getTime()); // Yeniden eskiye

/**
 * Verilen tarihteki geçerli aylık asgari ücreti döndürür (TL)
 */
export function getAsgariUcret(tarih: Date): number {
  for (const entry of ASGARI_UCRET_TABLOSU) {
    if (tarih >= entry.tarih) return entry.aylik;
  }
  return ASGARI_UCRET_TABLOSU[ASGARI_UCRET_TABLOSU.length - 1].aylik;
}

/**
 * Günlük asgari ücret (aylık / 30)
 */
export function getGunlukAsgariUcret(tarih: Date): number {
  return getAsgariUcret(tarih) / 30;
}
